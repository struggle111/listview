package com.swater.meimeng.mutils.sound.sundButton;

public class CanNotRecordException extends Exception {

	private static final long serialVersionUID = -8963740559492341970L;

	public CanNotRecordException() {
		super();
	}

	public CanNotRecordException(String detailMessage, Throwable throwable) {
		super(detailMessage, throwable);
	}

	public CanNotRecordException(String detailMessage) {
		super(detailMessage);
	}

	public CanNotRecordException(Throwable throwable) {
		super(throwable);
	}

}
